public class BloodTestResult {
    private double heamoglobin;
    private double ureaCreatinine;
    private double totalCholestrol;
    private double highDensityLipoProtein;
    private double lowDensityLipoProtein;
    private double triglycerides;
    private double randomPlasmaGlucose;
    private double fastingPlasmaGlucose;
    private double postPrandialPlasmaGlucose;

    public double getHeamoglobin() {
        return heamoglobin;
    }

    public void setHeamoglobin(double heamoglobin) {
        this.heamoglobin = heamoglobin;
    }

    public double getUreaCreatinine() {
        return ureaCreatinine;
    }

    public void setUreaCreatinine(double ureaCreatinine) {
        this.ureaCreatinine = ureaCreatinine;
    }

    public double getTotalCholestrol() {
        return totalCholestrol;
    }

    public void setTotalCholestrol(double totalCholestrol) {
        this.totalCholestrol = totalCholestrol;
    }

    public double getHighDensityLipoProtein() {
        return highDensityLipoProtein;
    }

    public void setHighDensityLipoProtein(double highDensityLipoProtein) {
        this.highDensityLipoProtein = highDensityLipoProtein;
    }

    public double getLowDensityLipoProtein() {
        return lowDensityLipoProtein;
    }

    public void setLowDensityLipoProtein(double lowDensityLipoProtein) {
        this.lowDensityLipoProtein = lowDensityLipoProtein;
    }

    public double getTriglycerides() {
        return triglycerides;
    }

    public void setTriglycerides(double triglycerides) {
        this.triglycerides = triglycerides;
    }

    public double getRandomPlasmaGlucose() {
        return randomPlasmaGlucose;
    }

    public void setRandomPlasmaGlucose(double randomPlasmaGlucose) {
        this.randomPlasmaGlucose = randomPlasmaGlucose;
    }

    public double getFastingPlasmaGlucose() {
        return fastingPlasmaGlucose;
    }

    public void setFastingPlasmaGlucose(double fastingPlasmaGlucose) {
        this.fastingPlasmaGlucose = fastingPlasmaGlucose;
    }

    public double getPostPrandialPlasmaGlucose() {
        return postPrandialPlasmaGlucose;
    }

    public void setPostPrandialPlasmaGlucose(double postPrandialPlasmaGlucose) {
        this.postPrandialPlasmaGlucose = postPrandialPlasmaGlucose;
    }
}
