public class UserEventDetails {
    private String iswalked;
    private String isCycled;
    private String isSwimmed;
    private String doneAerobics;
    private String others;
    private String date;
    private String uid;

    public String getIswalked() {
        return iswalked;
    }

    public void setIswalked(String iswalked) {
        this.iswalked = iswalked;
    }

    public String getIsCycled() {
        return isCycled;
    }

    public void setIsCycled(String isCycled) {
        this.isCycled = isCycled;
    }

    public String getIsSwimmed() {
        return isSwimmed;
    }

    public void setIsSwimmed(String isSwimmed) {
        this.isSwimmed = isSwimmed;
    }

    public String getDoneAerobics() {
        return doneAerobics;
    }

    public void setDoneAerobics(String doneAerobics) {
        this.doneAerobics = doneAerobics;
    }

    public String getOthers() {
        return others;
    }

    public void setOthers(String others) {
        this.others = others;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

}
